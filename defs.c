struct tree *var_lookup(char *s, int mustbethere) {
    struct var *v;

    for (v = vars; v != 0; v = v->v_next)
        if (strcmp(s, v->v_ident) == 0)
            return v->v_tree;
    if (mustbethere)
        yyerror("unknown variable");
    return 0;
}

