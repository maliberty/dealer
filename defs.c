void insertshape(s, any, neg_shape) char s[4];
{
    int i, j, p;
    int xcount = 0, ccount = 0;
    char copy_s[4];

    for (i = 0; i < 4; i++) {
        if (s[i] == 'x')
            xcount++;
        else
            ccount += s[i] - '0';
    }
    switch (xcount) {
        case 0:
            if (ccount != 13)
                yyerror("wrong number of cards in shape");
            for (p = 0; p < (any ? 24 : 1); p++)
                setshapebit(s[perm[p][3]] - '0', s[perm[p][2]] - '0', s[perm[p][1]] - '0', s[perm[p][0]] - '0',
                            1 << shapeno, neg_shape);
            break;
        default:
            if (ccount > 13)
                yyerror("too many cards in ambiguous shape");
            bcopy(s, copy_s, 4);
            for (i = 0; copy_s[i] != 'x'; i++)
                ;
            if (xcount == 1) {
                copy_s[i] = 13 - ccount + '0'; /* could go above '9' */
                insertshape(copy_s, any, neg_shape);
            } else {
                for (j = 0; j <= 13 - ccount; j++) {
                    copy_s[i] = j + '0';
                    insertshape(copy_s, any, neg_shape);
                }
            }
            break;
    }
}

